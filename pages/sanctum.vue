  <h2>
    2. Liquid staking
  </h2>

<p>
  Validation rewards are given to
validators every epoch (a defined amount of transactions validated). When the
rewards are given, the token can be released and new tokens can be locked.
The consequence is that staking tokens requires you to lock your
Solanas for 2 to 3 days (432000 slots).
From a user perspective, it makes the staking experience unpleasant.
</p>

<p>
That's where Liquid staking comes into play. Some protocols propose to aggregate
SOLs from many wallets and stake them for users in exchange for a token
  labeled by the protocol, a receipts token. Because the protocol collects
many native tokens, they can easily manage the in and out of the stakers.
  Moreover, they can use the most efficient strategy to stake the tokens (each
validator has their own characteristics, strategies are possible to get a
good balance). That way, they
can take a fee on staked tokens while providing a good APY to the user.
</p>

<p>
It improves a lot the user experience but it adds a layer that can be
compromised. And, it doesn't allow the user to select its validator. Validators
are often tied to non-LST projects.
</p>

<p>
It's common for Solana projects like JUP, Helium, Bonk to get their own validator.
  Because people want to be able to support the project while
keeping their sols, they like by staking their Sols on the project validator.
  With current Liquid Staking Providers, it's not possible to get the benefits
of liquid staking and select a favorite validator.
</p>

<img
  class="illustration"
  src="~/assets/images/articles/sanctum/liquid-staking.png"
/>

  <h2>
    3. Liquid staking for any project
  </h2>

<p>
This is where comes the Sanctum innovation. The Sanctum protocol allows any project to provide its
own liquid staking. That way, you can stake your sols into your favorite project
  validator while keeping the benefits of liquid staking. It's even better
    because you deal directly with the desired validator, thus, you limit the
  compromising risk of the LST token.
</p>

<p>
As an example, you can buy jupSols to stake on the Jupiter validator or buy
bonkSols to stake on the Bonk validator. You can support the Bonk team without
being exposed to the BONK volatility.
</p>

<p>
If you want to support Sanctum directly, you can use their own receipt token:
  the Infinity token ($INF).
INF acts as a classic LST token where they give you a receipt for managing your
staked sols for you. It comes with additional rewards from the fees used to
  trade Sanctum LSTs.
</p>

<img class="illustration"  style="max-width: 400px;" src="~/assets/images/articles/sanctum/sanctum.png" />

