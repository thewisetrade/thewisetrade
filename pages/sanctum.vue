  <h1>Sanctum Guide</h1>

  <h2>
    1. Validator staking
  </h2>

<p>
Staking is at the core of every blockchain based on the Proof Of Stake
mechanism.
  Solana is no exception. Staking is a principle where users of
the blockchain lend their tokens to validator servers. Validators are
computers verifying that every block (group of transactions) pushed to
Solana is valid and can be safely added to the
blockchain ledger.
</p>

<p>
The more a validator owns tokens, the more it is considered
as legit by the algorithm to
validate blocks. Every block validated by the validator is rewarded. So, the
more the validator validates transactions, the more fees it collects.
This system creates a strong incentive for validators to attract a lot of lenders.
</p>

<p>
To stake on a validator, you can use directly your wallet where a list of validators is available.
  They all have different settings: fees collected and total value locked.
  Validators with bigger amounts of SOLs tend to be safer but grab more fees
most of the time. It's up to you to find the right balance between safety and
rewards by using different validators.
</p>

<p>
Staking comes with rewards. All stakers get a share of the fees collected by
the validators. Every staked receives around 8% of the staked amount
on Average Per Year (APY). Rewards can be retrieved aside from unstaking.
</p>

<p>
<em>
Note: It leads to a secure blockchain because a validator compromising the chain
would lose all its tokens. The cost of an attack is too high to make it
interesting.
</em>
</p>

