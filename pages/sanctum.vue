bucket is represented by a cute character gaining XP when you hold your
linked LST.
</p>

<p>
They also created community quests. On a
  regular basis, the community meets around a complex riddle and solves it
collaboratively. Once solved, extra point rewards are collected for a given set
  of LSTs.
</p>

