tensorSOLs. It would allow you to bid and trade while still taking full
 advantage of the staking rewards and contributing to the Solana blockchain and
 the Tensor project. The same mechanism works for Kamino, Magic Eden,
   SharkyFi or any project requiring to use of your SOLs.
</p>

